#include "DOF.h"

////////////////////////////////////////////////////////////////////////////////

DOF::DOF(float value, float min, float max) {
   
    // Initialize constant data
    this->value = value;
    this->min = min;
    this->max = max;

}

////////////////////////////////////////////////////////////////////////////////

void DOF::SetValue( float val ) {
    
    if (val > max)
        value = max;
    else if (val < min)
        value = min;
    else
        value = val;
}

////////////////////////////////////////////////////////////////////////////////

float DOF::GetValue() {

    return value;
}

////////////////////////////////////////////////////////////////////////////////

void DOF::SetMinMax(float min, float max) {

    this->min = min;
    this->max = max;
}

